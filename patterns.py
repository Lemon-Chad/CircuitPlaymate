def mg(x):
    if x < 0: return 0
    elif 10 < x: return 1
    return max(0, math.sin(math.pi * x / 20))

def mf(x):
    if x < 0: return 1
    elif 10 < x: return 0
    return max(0, math.cos(math.pi * x / 20))

def get_mood_color(x):
    return (
        round(255 * mf(x)),
        round(255 * mg(x)),
        0
    )

def get_mood_colors(y):
    colors = []
    for i in range(11):
        colors.append(get_mood_color(i + y))
    return colors


